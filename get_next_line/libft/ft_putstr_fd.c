#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*cpy;

	if (s)
	{
		cpy = (char*)s;
		while (*cpy)
		{
			ft_putchar_fd(*cpy, fd);
			cpy++;
		}
	}
}
